# Каждый символ в строке имеет свой порядковый
# Нуммерация начинается с 0
# Обращение к символу

word = "Python"

print(word[0]) # вывод Р самый левый (первый) символ строки
print(word[4]) # вывод 0 - (пятый символ)
print(word[1]) # вывод у - символ с индексом 1, но по порядку он 2-й с лева
print('===========')

# Отрицательные индексы

print(word[-1]) # Вывод n - последний (самый правый) символ в строке
print(word[-2]) # Вывод о - второй справа символ ( предпоследний)


# Конкатенация (соединение) строк
# Операция + используется только с одинаковыми числами данных
# для чисел+ выполняет сложение
# для строк + выполняет конкатенацию

first_name = 'Иван'
last_name = "Петров"
full_name = first_name +  ""  + last_name
print(full_name) # Иван Петров

# Если нужно соединить строку и число
age = 25
