# Срезы строк (выделение подстроки)
text = "Программирование"
# Первый индекс включается, а второй не включительно
print(text[0:6]) # Вывод Програ (символы с 0 по 5)

sub_string = text[0:2]
print("sub:",sub_string ) # вывод Пр - выделена строка начиная с индекса 0(вкл) до 2 ( не включительно)
print(text[:6]) # Вывод Програ (символы с 0 по 5)
print(text[6:]) # вывод ммирование начиная с 6 (вкл) и до конца строки
print(text[6:10]) # вывод ммир (6,10) с 6 (вкл) до 10 (10-1) включительно

