"""
Task 2: Склад и коробки
Условие:
Есть склад размером 30×40 и высотой 20, размер коробки – 5×8×4.
Напишите программу, которая сохраняет эти данные в переменных, вычисляет и выводит в консоль, сколько коробок поместится на склад.
"""

"""
Алгоритм решения:
1. Сохранить размеры склада и коробки в переменные.
2. Вычислить общий объём склада и объём одной коробки.
3. Разделить объём склада на объём коробки, чтобы получить количество коробок.
4. Вывести результат.
"""

# Размеры склада
storage_length = 30
storage_width = 40
storage_height = 20

# Размеры коробки
box_length = 5
box_width = 8
box_height = 4

# Вычисляем объём
storage_volume = storage_length * storage_width * storage_height
box_volume = box_length * box_width * box_height

# Вычисляем количество коробок
boxes = storage_volume // box_volume

# Вывод результата
print("На склад поместится", boxes, "коробок")
