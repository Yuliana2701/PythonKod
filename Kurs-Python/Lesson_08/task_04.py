"""
Напишите функцию с тремя параметрами - первое число, второе число и действие.

Действие может быть +, -, * или /.

В зависимости от переданного третьего параметра программа должна выполнить соответствующее действие
с двумя числами и вернуть результат.

Запросите эти данные у пользователя, вызовите функцию и выведите результат на экран.
"""

def calculator(num1, num2, operation):
    if operation == "+":
        return num1 + num2
    elif operation == "-":
        return num1 - num2
    elif operation == "*":
        return num1 * num2
    elif operation == "/":
        if num2 != 0:
            return num1 / num2
        else:
            return "Ошибка: деление на ноль!"
    else:
        return "Ошибка: неизвестная операция!"

# Примеры вызова функции
print(calculator(10, 5, "+"))  # 15
print(calculator(10, 0, "/"))  # Ошибка: деление на ноль!
